////////////////////////////////////////////////////////////////////////////////////////////////////
/// <summary>   Description of the column. </summary>
///
/// <remarks>   Law Metzler, 7/25/2024. </remarks>
///
/// <param name="name">         The name. </param>
/// <param name="cleanedName">  Name of the cleaned. </param>
/// <param name="type">         The type. </param>
/// <param name="index">        Zero-based index of the. </param>
////////////////////////////////////////////////////////////////////////////////////////////////////

public class ColumnDescription(string name, string cleanedName, string? type, int index)
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Gets the name of the column. </summary>
    ///
    /// <value> The name of the column. </value>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public string ColumnName { get; } = name;

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Gets or sets the type of the column. </summary>
    ///
    /// <value> The type of the column. </value>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public string? ColumnType { get; private set; } = type;

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Gets or sets the name of the cleaned. </summary>
    ///
    /// <value> The name of the cleaned. </value>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public string CleanedName { get; private set; } = cleanedName;

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Gets or sets the zero-based index of the column. </summary>
    ///
    /// <value> The column index. </value>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public int ColumnIndex { get; private set; } = index;
    /// <inheritdoc />

    public override string ToString()
    {
        return ColumnName;
    }
}